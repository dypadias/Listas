    private void cx1jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cx1jButton4ActionPerformed

        List<Senha> filtro = listSenha.stream().filter(x -> x.toString().charAt(0) == 'P').collect(Collectors.toList());

        itr = filtro.iterator();
        if (!filtro.isEmpty()) {
            while (itr.hasNext()) {
                Senha p = itr.next();

                exbCaixajLabel3.setText("CX-1");
                exbCaixajLabel3.setForeground(Color.green);
                senhaCaixasjLabel2.setForeground(Color.green);
                senhaCaixasjLabel2.setText(p.toString());

                filtro.remove(p);
                listSenha.remove(p);
                break;
            }

        } else if (!itr.hasNext()) {
            itr = listSenha.iterator();
            while (itr.hasNext()) {

                Senha snh = itr.next();
                exbCaixajLabel3.setText("CX-1");
                senhaCaixasjLabel2.setText(snh.toString());

                itr.remove();
                break;

            }
        }


    }//GEN-LAST:event_cx1jButton4ActionPerformed

